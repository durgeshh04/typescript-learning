// Number Type in TypeScript
// In TypeScript, the number type is used to represent both integers and floating-point numbers. It supports:

// Decimal (10)
// Hexadecimal (0xff)
// Binary (0b1010)
// Octal (0o744)


let decimal: number = 42;          // Decimal number
let hex: number = 0xff;            // Hexadecimal
let binary: number = 0b1010;        // Binary
let octal: number = 0o744;         // Octal
let float: number = 3.14;          // Floating-point
let big: number = 1_000_000;       // Underscores for readability

console.log(decimal, hex, binary, octal, float, big);
